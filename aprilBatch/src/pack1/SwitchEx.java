package pack1;

public class SwitchEx {

	public static void main(String[] args) {
		
		int x =4;
		
		switch(x) {
		
		case 0:
			System.out.println("Value of x is Zero");
			break;
		case 1:
			System.out.println("Value of x is One");
			break;
		case 4:
			System.out.println("Value of x is Four");
			break;
		case 5:
			System.out.println("Value of x is Five");
			break;
			
		default:
			System.out.println("Case not matched");
		}

	}

}
