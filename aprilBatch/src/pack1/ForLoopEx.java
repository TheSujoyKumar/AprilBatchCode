package pack1;

public class ForLoopEx {

	public static void main(String[] args) {
		
		/*
		 * for(int x=1;x<10;x++) { System.out.println(x); }
		 */
		
		/*
		 * int x=1; for(;x<10;x++) { System.out.println(x); }
		 */
		
		/*
		 * int x=1; for(;;x++) { System.out.println(x); }
		 */
		
		int x=1;
		for(;;) {
			System.out.println(x);
		}
	}

}
